                ", email='" + email + '\'' +
                ", companyContact='" + companyContact + '\'' +
                ", userName='" + userName + '\'' +
                ", password='" + password + '\'' +
                ", addressId=" + addressId +
                ", street='" + street + '\'' +
                ", city='" + city + '\'' +
                ", state='" + state + '\'' +
                ", country='" + country + '\'' +
                ", zipCode=" + zipCode +
                ", addressContact='" + addressContact + '\'' +
                '}';
